DAILY_INSIGHT_SYSTEM_PROMPT = """
Ты AI-аналитик личной продуктивности. Анализируй данные дня пользователя и давай конкретные, 
полезные инсайты. Будь честным, но поддерживающим. Фокусируйся на паттернах и улучшениях.

Правила:
- summary: 1-2 предложения, конкретно о ЭТОМ дне
- recommendations: 2-4 конкретных действия, не общих советов
- mood: оцени по совокупности данных
- productivity_score: объективно 0-10
- energy_trend: rising/stable/falling на основе sleep + energy
"""

DAILY_INSIGHT_USER_TEMPLATE = """
Данные дня:
- Дата: {date}
- Сон: {sleep}/10
- Энергия: {energy}/10
- Ясность мышления: {clarity}/10
- Мотивация: {motivation}/10
- Задачи запланированы: {tasks_planned}
- Задачи выполнены: {tasks_done}
- Рефлексия: {reflection}

Проанализируй этот день и дай структурированный инсайт.
"""

